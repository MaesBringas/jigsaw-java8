public class FootballPlayer {
    String name, surname, position, team;
    int age;

    public FootballPlayer(){
    }

    public FootballPlayer(String name, String surname, String position, String team, int age) {
        this.name = name;
        this.surname = surname;
        this.position = position;
        this.team = team;
        this.age = age;
    }


}
